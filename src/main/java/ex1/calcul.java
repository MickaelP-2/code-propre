package ex1;
/**
 * classe de calcul avec deux nombres a,b
 * opérations: addition et soustraction
 */
public class calcul {
	/**
	 * addition pour additionenr deux nombres passées en paramètres
	 * de la fonction (renommée en addition)
	 * @param a
	 * @param b
	 * @return a+b
	 */
	public int addition(int a, int b){
		return a+b;
	}
	/**
	 * soustraction de deux nombres passés en paramètres
	 * de la fonction soustraction (renommée en soustraction)
	 * @param a
	 * @param b
	 * @return a-b
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
